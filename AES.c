uchar *g(uchar bytes[4], int round) {
    static uchar val[4];
    val[0] = bytes[1];
    val[1] = bytes[2];
    val[2] = bytes[3];
    val[3] = bytes[0];
    for(int i = 0; i < 4; i++) {
        val[i] = sBoxTable[val[i]]; //Use switch box on each byte
    }
    val[0] = val[0] ^ rcon[round];
    return val;
}


