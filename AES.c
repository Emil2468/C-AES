Block Decrypt(Block key, Block chiferText) {
    Block keys[11];
    keys[0] = key;
    for(int i = 1; i < 11; i++) {
        keys[i] = generateNewKey(keys[i - 1], i);
    }
    Block output = KeyAddition(chiferText, keys[10]);
    output = InvShiftRows(output);
    output = InvSubstitute(output);

    for(int i = 9; i >= 1; i--) {
        output = KeyAddition(output, keys[i]);
        output = InvMixColumns(output);
        output = InvShiftRows(output);
        output = InvSubstitute(output);
    }

    output = KeyAddition(output, keys[0]);

    return output;
}

Block Substitute(Block input) {
    Block output;
    
    for(int i = 0; i < 16; i++)
    {
        output.bytes[i] = sBoxTable[input.bytes[i]];
    }
    return output;
}

Block InvSubstitute(Block input) {
    Block output;
    
    for(int i = 0; i < 16; i++)
    {
        output.bytes[i] = invSboxTable[input.bytes[i]];
    }
    return output;
}

Block ShiftRows(Block input) {
    Block output;
    int n = 0;
    for(int i = 0; i < 4; i++) {
        for(int j = 0; j < 4; j++) {
            output.bytes[n] = input.bytes[(n + j * 4) % 16];
            n++;
        }
    }
    return output;
}

Block InvShiftRows(Block input) {
    Block output;
    int n = 0;
    for(int i = 0; i < 4; i++) {
        for(int j = 0; j < 4; j++) {
            output.bytes[(n + j * 4) % 16] = input.bytes[n];
            n++;
        }
    }
    return output;
}

Block MixColumns(Block input) {
    Block output;
    for(int i = 0; i < 16; i+= 4) {
        output.bytes[i + 0] = gmul(input.bytes[i], 2) ^ gmul(input.bytes[i + 1], 3) ^ gmul(input.bytes[i + 2], 1) ^ gmul(input.bytes[i + 3], 1);
        output.bytes[i + 1] = gmul(input.bytes[i], 1) ^ gmul(input.bytes[i + 1], 2) ^ gmul(input.bytes[i + 2], 3) ^ gmul(input.bytes[i + 3], 1);
        output.bytes[i + 2] = gmul(input.bytes[i], 1) ^ gmul(input.bytes[i + 1], 1) ^ gmul(input.bytes[i + 2], 2) ^ gmul(input.bytes[i + 3], 3);
        output.bytes[i + 3] = gmul(input.bytes[i], 3) ^ gmul(input.bytes[i + 1], 1) ^ gmul(input.bytes[i + 2], 1) ^ gmul(input.bytes[i + 3], 2);
    }
    return output;
}

Block InvMixColumns(Block input) {
    Block output;
    for(int i = 0; i < 16; i+= 4) {
        output.bytes[i + 0] = gmul(input.bytes[i], 0x0e) ^ gmul(input.bytes[i + 1], 0x0b) ^ gmul(input.bytes[i + 2], 0x0d) ^ gmul(input.bytes[i + 3], 0x09);
        output.bytes[i + 1] = gmul(input.bytes[i], 0x09) ^ gmul(input.bytes[i + 1], 0x0e) ^ gmul(input.bytes[i + 2], 0x0b) ^ gmul(input.bytes[i + 3], 0x0d);
        output.bytes[i + 2] = gmul(input.bytes[i], 0x0d) ^ gmul(input.bytes[i + 1], 0x09) ^ gmul(input.bytes[i + 2], 0x0e) ^ gmul(input.bytes[i + 3], 0x0b);
        output.bytes[i + 3] = gmul(input.bytes[i], 0x0b) ^ gmul(input.bytes[i + 1], 0x0d) ^ gmul(input.bytes[i + 2], 0x09) ^ gmul(input.bytes[i + 3],0x0e);
    }
    return output;
}

Block KeyAddition(Block input, Block key) {
    Block output;
    for(int i = 0; i < 16; i++) {
        output.bytes[i] = key.bytes[i] ^ input.bytes[i];
    }
    return output;
}


