Block generateNewKey(Block oldKey, int round) {
    uchar oldGroups[4][4];
    uchar newGroups[4][4];
    Block newKey;
    int n = 0;
    for(int i = 0; i < 4; i++) { 
        for(int j = 0; j < 4; j++)
        {
            oldGroups[i][j] = oldKey.bytes[n];
            n++;
        }        
    }
    uchar *group4AfterG = g(oldGroups[3], round);
    for(int i = 0; i < 4; i++) {
        newGroups[0][i] = oldGroups[0][i] ^ *(group4AfterG + i);
    }
    for(int i = 1; i < 4; i++) {
        for(int j = 0; j < 4; j++) {
            newGroups[i][j] = oldGroups[i][j] ^ newGroups[i - 1][j]; 
        }
    }
    n = 0;
    for(int i = 0; i < 4; i++) { 
        for(int j = 0; j < 4; j++)
        {
            newKey.bytes[n] = newGroups[i][j];
            n++;
        }        
    }
    return newKey;
}

